_FLAT_SEASONALITY = [1.0] * 12


def _get_seasonality(disease: str) -> List[float]:
    return MONTH_SEASONALITY.get(disease.lower(), _FLAT_SEASONALITY)


def generate_monthly_series(
    disease: str,
    country: str,
    start_date: Optional[date] = None,
    end_date: Optional[date] = None,
) -> List[Dict[str, Any]]:
    """
    Generate a monthly time-series for a disease/country combination
    using interpolated annual anchor data.

    Returns a list of {date, cases, deaths, source, is_cumulative} dicts
    where `is_cumulative` is False (monthly incidence counts).
    """
    anchors = GLOBAL_ANCHORS.get(disease.lower())
    if not anchors:
        return []

    scale = _get_country_scale(country, disease)
    if scale <= 0:
        return []

    anchor_years = sorted(anchors.keys())
    if start_date is None:
        start_date = date(anchor_years[0], 1, 1)
    if end_date is None:
        end_date = date.today()

    start_year = start_date.year
    end_year = end_date.year

    interp = _interpolate_annual(anchors, start_year, end_year)
    seasonality = _get_seasonality(disease)

    rng = random.Random(hash(disease + country) & 0xFFFF_FFFF)

    points: List[Dict[str, Any]] = []
    # Track cumulative totals for growth-rate computation.
    cum_cases = 0
    cum_deaths = 0

    current = date(start_year, 1, 1)
    while current <= end_date:
        year = current.year
        month = current.month

        annual_cases, annual_deaths = interp.get(year, (0.0, 0.0))
        monthly_base_cases = (annual_cases * scale) / 12.0
        monthly_base_deaths = (annual_deaths * scale) / 12.0

        # Apply seasonality.
        season_mul = seasonality[month - 1]
        raw_cases = monthly_base_cases * season_mul
        raw_deaths = monthly_base_deaths * season_mul

        # ±15 % random jitter for realism.
        jitter_c = 1.0 + rng.uniform(-0.15, 0.15)
        jitter_d = 1.0 + rng.uniform(-0.15, 0.15)

        monthly_cases = max(0, int(raw_cases * jitter_c))
        monthly_deaths = max(0, int(raw_deaths * jitter_d))

        # Accumulate.
        cum_cases += monthly_cases
        cum_deaths += monthly_deaths

        if current >= start_date:
            points.append({
                "date": current.isoformat(),
                "cases": monthly_cases,
                "deaths": monthly_deaths,
                "source": "reference_data",
                "is_cumulative": False,
            })

        # Advance by one month.
        if month == 12:
            current = date(year + 1, 1, 1)
        else:
            current = date(year, month + 1, 1)

    return points


def get_reference_series(
    disease: str,
    country: str,
) -> List[Dict[str, Any]]:
    """
    Public entry point: return the reference monthly series for a disease/country.
    Returns [] if no reference data is available for the disease.
    """
    return generate_monthly_series(disease, country)

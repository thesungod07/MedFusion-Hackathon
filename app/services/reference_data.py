# Default scale for countries not explicitly listed.
_DEFAULT_SCALE = 0.02


def _get_country_scale(country: str, disease: str) -> float:
    country_map = COUNTRY_SCALE.get(country)
    if country_map is None:
        # Try case-insensitive match.
        for k, v in COUNTRY_SCALE.items():
            if k.lower() == country.lower():
                country_map = v
                break
    if country_map is None:
        return _DEFAULT_SCALE
    return country_map.get(disease.lower(), _DEFAULT_SCALE)


def _lerp(a: float, b: float, t: float) -> float:
    return a + (b - a) * t


def _interpolate_annual(
    anchors: Dict[int, Tuple[int, int]],
    start_year: int,
    end_year: int,
) -> Dict[int, Tuple[float, float]]:
    """Linear interpolation between anchor years."""
    sorted_years = sorted(anchors.keys())
    result: Dict[int, Tuple[float, float]] = {}

    for year in range(start_year, end_year + 1):
        # Find surrounding anchors.
        prev_yr = max((y for y in sorted_years if y <= year), default=sorted_years[0])
        next_yr = min((y for y in sorted_years if y >= year), default=sorted_years[-1])
        if prev_yr == next_yr:
            result[year] = (float(anchors[prev_yr][0]), float(anchors[prev_yr][1]))
        else:
            t = (year - prev_yr) / (next_yr - prev_yr)
            c = _lerp(anchors[prev_yr][0], anchors[next_yr][0], t)
            d = _lerp(anchors[prev_yr][1], anchors[next_yr][1], t)
            result[year] = (c, d)

    return result


